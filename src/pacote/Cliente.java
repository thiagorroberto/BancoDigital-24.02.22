package pacote;

public class Cliente {
	private String nome;
	protected int assinatura;

	public int getAssinatura() {
		return assinatura;
	}

	public void setAssinatura(int assinatura) {
		this.assinatura = assinatura;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
